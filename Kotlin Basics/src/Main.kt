// === Entry Point in Kotlin ===

// Definition
// The 'main' function is the starting point of a Kotlin application.
// When the program runs, the code inside this function executes first.

// Usage
// Use the 'main' function to contain the initial code your application runs.
// Use 'println()' to print messages to the console.

// Example
fun main() {
    println("Hello, World!") // Output: Hello, World!
}

// How It Works
// - The Kotlin runtime looks for the 'main' function to start execution.
// - 'println()' prints the provided message followed by a newline.

// Notes & Tips
// - Every Kotlin program requires a 'main' function as the entry point.
// - Use 'println()' for outputting information and debugging.
