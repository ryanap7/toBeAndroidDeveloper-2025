fun main() {
    // === Function Definition and Invocation in Kotlin ===
    // Definition:
    // A function is a block of code designed to perform a specific task.
    // Functions help organize code, improve readability, and promote reusability.

    // Usage:
    // Define a function using the 'fun' keyword, followed by the function name and parentheses.
    // Call (invoke) the function by writing its name followed by parentheses.

    // Example:
    helloWorld() // Calls the function to execute its code

    // How It Works:
    // 1. 'helloWorld' is defined below, containing a println statement.
    // 2. When called inside 'main', the function body is executed.
    // 3. The console prints "Hello World!".

    // Notes & Tips:
    // - Functions can take parameters and return values (not shown here).
    // - Place function definitions above or below 'main', as long as they are in scope.
    // - Keep functions focused on a single task for better maintainability.
}

// Function definition
fun helloWorld() {
    println("Hello World!") // Prints "Hello World!" to the console
}
