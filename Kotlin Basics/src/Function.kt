// === Function Definition and Invocation in Kotlin ===
// A function groups a set of statements to perform a specific task.
// Functions help organize code and promote reusability.

fun helloWorld() {
    println("Hello World!") // Prints "Hello World!" to the console
}

fun main() {
    // === Calling a Function ===
    // Invoke the 'helloWorld' function to execute its code block.
    helloWorld()

    // === Notes & Tips ===
    // - Define functions using 'fun' keyword followed by the function name and parentheses.
    // - Functions can take parameters and return values (not shown here).
    // - Calling a function runs its code wherever it is invoked.
}
