fun main() {
    // === Character (Char) Type in Kotlin ===
    // Definition:
    //   'Char' is a data type that stores a single character.
    //   Characters are enclosed in single quotes, for example: 'A', '1', '?'.
    //   Kotlin's Char uses 16-bit Unicode, so it can represent any Unicode character.
    //
    // Usage:
    //   Use Char when you need to store and work with single characters.
    //
    // Example:
    val char: Char = '?' // Store the question mark character
    println(char) // Output: ?
    //
    // How It Works:
    //   Char stores the numeric Unicode value of the character.
    //   When printed, Kotlin automatically converts it to its visual representation.
    //
    // Notes & Tips:
    //   - Use Char for single characters only.
    //   - For multiple characters, use String instead.
}
